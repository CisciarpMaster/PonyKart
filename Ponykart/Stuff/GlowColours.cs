
namespace Ponykart {

	/// <summary>
	/// Colours available for balloon glow.
	/// 
	/// If you're using these, remember to put the entity into the RENDER_QUEUE_BUBBLE_GLOW render queue!
	/// </summary>
	public enum BalloonGlowColour {
		red,
		blue,
		yellow,
		green,
		orange,
		magenta,
		purple,
		cyan,
		white
	}

	/// <summary>
	/// Colours available for flat glow.
	/// 
